package Enum;

/**
 * A Show status enum
 * 
 * <p>A <code>ShowStatus enum</code> that has 
 * value of <code> COMINGSOON,PREVIEW,SHOWING,ENDOFSHOWING</code> </p>
 * 
 */

public enum ShowStatusEN {
	/**
	 * The show is coming soon
	 */
	COMINGSOON,
	/**
	 * The show preview
	 */
	PREVIEW,
	/**
	 * The show is currently playing
	 */
	SHOWING,
	/**
	 * The show is no longer playing
	 */
	ENDOFSHOWING
}